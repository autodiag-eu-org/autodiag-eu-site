function translateName(code, frName) {
  // Use exact code-to-name dictionary first
  if (codeNameEN[code]) return codeNameEN[code];
  // Fallback to cause dictionary then phrase translation
  const translated = translateCauseName(frName);
  if (translated !== frName) return translated;
  return translatePhrase(frName);
}

