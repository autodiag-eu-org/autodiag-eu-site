function calcGBP(eurPrice) {
  // EUR to GBP roughly 0.85, with slight random variation (0.82-0.88)
  const factor = 0.82 + Math.random() * 0.06;
  return Math.round(eurPrice * factor);
}

// Seed random for reproducibility
let seed = 42;
function seededRandom() {
  seed = (seed * 16807) % 2147483647;
  return (seed - 1) / 2147483646;
}

function calcGBPSeeded(eurPrice) {
  const factor = 0.82 + seededRandom() * 0.06;
  return Math.round(eurPrice * factor);
}

