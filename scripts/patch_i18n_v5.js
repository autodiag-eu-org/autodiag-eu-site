const allNamespaces = { about, compatibility, savings, pricing, comparison };

for (const lang of LANGS) {
  const filePath = path.join(BASE, "i18n", `${lang}.json`);
  const data = JSON.parse(fs.readFileSync(filePath, "utf8"));

  for (const [ns, translations] of Object.entries(allNamespaces)) {
    const langData = translations[lang];
    if (!langData) {
      console.error(`Missing ${lang} data for namespace ${ns}`);
      continue;
    }
    if (!data[ns]) {
      data[ns] = {};
    }
    // Merge new keys (don't overwrite existing ones)
    for (const [key, value] of Object.entries(langData)) {
      if (!(key in data[ns])) {
        data[ns][key] = value;
      }
    }
  }

  fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + "\n", "utf8");
  console.log(`Patched ${lang}.json`);
}

