function translatePhrase(frText) {
  let result = frText;

  // Sort dictionary by length descending to match longer phrases first
  const sorted = Object.entries(frToEn).sort((a, b) => b[0].length - a[0].length);

  for (const [fr, en] of sorted) {
    const regex = new RegExp(escapeRegex(fr), 'gi');
    result = result.replace(regex, en);
  }

  // Clean up any remaining French connectors
  result = result
    .replace(/\bdu\b/gi, 'of the')
    .replace(/\bde la\b/gi, 'of the')
    .replace(/\bde l'/gi, 'of the ')
    .replace(/\bd'/gi, 'of ')
    .replace(/\bdes\b/gi, 'of the')
    .replace(/\bde\b/gi, 'of')
    .replace(/\ble\b/gi, 'the')
    .replace(/\bla\b/gi, 'the')
    .replace(/\bles\b/gi, 'the')
    .replace(/\bou\b/gi, 'or')
    .replace(/\bet\b/gi, 'and')
    .replace(/\bsur\b/gi, 'on')
    .replace(/\bpar\b/gi, 'by')
    .replace(/\btres\b/gi, 'very')
    .replace(/\btrop\b/gi, 'too')
    .replace(/\bpas\b/gi, 'not')
    .replace(/\bplus\b/gi, 'more')
    .replace(/\bavec\b/gi, 'with')
    .replace(/\bsans\b/gi, 'without')
    .replace(/\ben\b/gi, 'in')
    .replace(/\ba\b/gi, 'at')
    .replace(/  +/g, ' ')
    .trim();

  // Capitalise first letter
  if (result.length > 0) {
    result = result.charAt(0).toUpperCase() + result.slice(1);
  }

  return result;
}

function escapeRegex(str) {
  return str.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

