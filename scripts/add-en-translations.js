// ─── DESCRIPTION GENERATION ───

function generateDescription(code, enName, category, index) {
  const templates = categoryDescTemplates[category] || categoryDescTemplates.moteur;
  const templateIdx = index % templates.length;
  return templates[templateIdx](code, enName);
}

// ─── MAIN ───

function main() {
  const raw = fs.readFileSync(INPUT, 'utf8');
  const codes = JSON.parse(raw);

  let processed = 0;

  for (let i = 0; i < codes.length; i++) {
    const dtc = codes[i];

    // Reset seed per code for reproducible GBP prices
    seed = hashCode(dtc.code);
    if (seed <= 0) seed = 42;

    // 1. Translate name
    const enName = translateName(dtc.code, dtc.name.fr);
    dtc.name.en = enName;

    // 2. Generate description
    dtc.description.en = generateDescription(dtc.code, enName, dtc.category, i);

    // 3. Translate causes + add GBP prices
    for (const cause of dtc.causes) {
      cause.name.en = translateCauseName(cause.name.fr);

      // GBP prices from FR prices
      if (cause.costMin.fr !== undefined) {
        cause.costMin.gb = calcGBPSeeded(cause.costMin.fr);
      }
      if (cause.costMax.fr !== undefined) {
        cause.costMax.gb = calcGBPSeeded(cause.costMax.fr);
      }
    }

    // 4. Translate symptoms
    if (dtc.symptoms && dtc.symptoms.fr) {
      dtc.symptoms.en = dtc.symptoms.fr.map(s => translateSymptom(s));
    }

    // 5. Generate FAQ
    dtc.faq.en = generateFAQ(dtc.code, enName, dtc.category, dtc.severity, dtc.canDrive, dtc.causes);

    // 6. MOT fail
    dtc.motFail = getMotFail(dtc.code, dtc.category, dtc.severity);

    processed++;
  }

  // Write back
  fs.writeFileSync(INPUT, JSON.stringify(codes, null, 2), 'utf8');

  return processed;
}

function hashCode(str) {
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    const char = str.charCodeAt(i);
    hash = ((hash << 5) - hash) + char;
    hash = hash & hash; // Convert to 32bit integer
  }
  return Math.abs(hash) || 42;
}

const count = main();
process.stdout.write(`Done. Processed ${count} DTC codes with English translations.\n`);
